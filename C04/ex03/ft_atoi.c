#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>

int ft_atoi(char *str)
{
    int oi;
    int i;
    int sign;

    sign = 1;
    oi = 0;
    i = 0;
    while ((str[i] == 32) || ((str[i] >= 8) && (str[i] <= 13)))
    {
        i++;
    }
    while ((str[i] == 45) || (str[i] == 43))
    {
        if (str[i] == 45)
        {
            sign = sign * - 1;
        }
        i++;
    }
    while ((str[i] >= '0') && (str[i] <= '9') && (str[i] != '\0'))
    {
        oi = oi * 10;
        oi = oi + str[i] - '0';
        i++;
    }
    return ((oi) * sign);
}
